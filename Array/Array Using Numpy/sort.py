import numpy as np
arr = np.array([5,2,4,1,3])
x = np.sort(arr)
print("1d array: ",x)
# sorting means putting the array in an ordered sequence.
# the ordered sequence can be numeric,alphabetical, ascending or descending.
# the numpy ndarray has a function called sort()
a = np.array(([2,3,1],[6,5,4]))
y = np.sort(a)
print("2d array: \n",y)